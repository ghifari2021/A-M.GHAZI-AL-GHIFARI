// String
let nama = "Ghifari";
console.log("Nama: " + nama);
alert("Nama: " + nama);

// Number
let usia = 21;
console.log("Usia: " + usia);
alert("Usia: " + usia);

// Boolean
let isMahasiswa = true;
console.log("Mahasiswa: " + isMahasiswa);
alert("Mahasiswa: " + isMahasiswa);

// Array
let hobi = ["Sepak bola", "Menonton film", "Bermain game"];
console.log("Hobi: " + hobi.join(", "));
alert("Hobi: " + hobi.join(", "));

// Object
let orang = {
  nama: "Ghifari",
  umur: 21,
  pekerjaan: "Mahasiswa",
};
console.log("Data Orang: ", orang);
alert("Nama: " + orang.nama + ", Umur: " + orang.umur + ", Pekerjaan: " + orang.pekerjaan);

// Null
let dataKosong = null;
console.log("Data Kosong: " + dataKosong);
alert("Data Kosong: " + dataKosong);

// Undefined
let dataBelumDiisi;
console.log("Data Belum Diisi: " + dataBelumDiisi);
alert("Data Belum Diisi: " + dataBelumDiisi);